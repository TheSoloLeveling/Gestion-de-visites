public class Demande {
    private String nom;
    private String prenom;
    private String cnie;
    private String date;
    private String time;
    private String entreprise;
    
    public String getNom() {
        return nom;
    }

    public String getPrenom() {
        return prenom;
    }

    public String getCnie() {
        return cnie;
    }

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }
    
    public String getEntreprise() {
        return entreprise;
    }
    
}






